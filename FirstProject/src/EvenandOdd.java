public class EvenandOdd {

	public static void main(String[] args) {

		int i;

		for (i = 0; i <= 20; i++) {

			if (i % 2 == 0) {
				System.out.println("Number  " + i);
				System.out.println("Is even number");
			} else {
				System.out.println("Number  " + i);
				System.out.println("Is odd number");
			}

		}

	}

}
